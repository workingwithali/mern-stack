var arr = [2,3,4,5]
/**
 * Iterates over each element in the array and appends " hello" to each element.
 * Note: The return value of the callback function is not used by forEach.
 *
 * @param {Array} arr - The array to iterate over.
 */
arr.forEach((var) => {
    return var + " hello";
});
print(arr)